package com.hebut.entity;

public class Donate {
	
	private int donateId;
	
	private String donateName;
	
	private float donateMoney;
	
	private String donateMessage;
	
	private String donateTime;

	public int getDonateId() {
		return donateId;
	}

	public void setDonateId(int donateId) {
		this.donateId = donateId;
	}

	public String getDonateName() {
		return donateName;
	}

	public void setDonateName(String donateName) {
		this.donateName = donateName;
	}

	public float getDonateMoney() {
		return donateMoney;
	}

	public void setDonateMoney(float donateMoney) {
		this.donateMoney = donateMoney;
	}

	public String getDonateMessage() {
		return donateMessage;
	}

	public void setDonateMessage(String donateMessage) {
		this.donateMessage = donateMessage;
	}

	public String getDonateTime() {
		return donateTime;
	}

	public void setDonateTime(String donateTime) {
		this.donateTime = donateTime;
	}
	
	

}
